/* You can output something to the console like this */
console.log("Hello World");
console.log(3.14)

// You also output calculations
console.log(1+1);
console.log(Math.pow(2,10));

console.log(Math.sqrt(4));

/* JavaScript is very error tolerant and tries to guess what you wanted. 
 * Even when you're mixing things up
 */
console.log("5" - 1);
console.log(5 + 1);

// This it can't guess
console.log("five" * 2);



process.exit(0);